package Week8.Inheritance.ClassWork;
public class Employee {

    private String name;
    private String address;


    void setName(String n){

        this.name = n;
    }

    void setAddress(String add){
        this.address = add;
    }

    public String getName(){
        return name;
    }

    public String getAddress(){
        return address;
    }

}
